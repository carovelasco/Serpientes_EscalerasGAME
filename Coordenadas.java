public class Coordenadas {
    private int fila;
    private int columna;
    
    public Coordenadas(int fila, int columna) {
        this.fila = fila;
        this.columna = columna;
    }
    
    public int getFila() {
        return fila;
    }
    
    public int getColumna() {
        return columna;
    }
}