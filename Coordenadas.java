public class Coordenadas {
    private int fila;
    private int columna;
    
    public Coordenadas(int pFila, int pColumna) {
        this.fila = pFila;
        this.columna = pColumna;
    }
    
    public int getFila() {
        return fila;
    }
    
    public int getColumna() {
        return columna;
    }
}